u8
p_noise2(s32 x, s32 y, s32 seed)
{
   u8 tmp = perlin_hash[(y + seed) % 256];
   return perlin_hash[(x + tmp) % 256];
}

f64 
perlin_inter(f64 x, f64 y, f64 s)
{
   return x + (s * s *(3-2*s)) * (y-x);
}

f64
perlin_noise_2d(f64 *cord, s32 seed)
{
   s32 floored[2] = {floor(cord[0]), floor(cord[1])};
   f64 frac[2]    = {cord[0] - floored[0], cord[1] - floored[1]};

   u8 s = p_noise2(floored[0],   floored[1],   seed);
   u8 t = p_noise2(floored[0]+1, floored[1],   seed);
   u8 u = p_noise2(floored[0],   floored[1]+1, seed);
   u8 v = p_noise2(floored[0]+1, floored[1]+1, seed);

   f64 low  = perlin_inter(s, t, frac[0]);
   f64 high = perlin_inter(u, v, frac[0]);
   return perlin_inter(low, high, frac[1]);
}

f64
perlin_noise(f64 x, f64 y, f64 freq, u16 depth, s32 seed)
{
   f64 a[2] = {x * freq, y * freq};
   f64 amp  = 1.0;
   f64 fin  = 0;
   f64 div  = 0.0;

   for (u8 i = 0; i < depth; i++)
   {
      div  += 256 * amp;
      fin  += perlin_noise_2d(a, seed) * amp;
      amp  /= 2;
      a[0] *= 2;
      a[1] *= 2;
   }

   return fin/div;
}
